#[derive(Debug, Clone, PartialEq)]
pub enum Token {
    Number(f64),
    Identifier(String),
    Plus,
    Minus,
    Star,
    Slash,
    Percent,
    Caret,
    LParen,
    RParen,
    LBrace,
    RBrace,
    LBracket,
    RBracket,
    Comma,
    Equal,
    EqualEqual,
    Less,
    Greater,
    LessEqual,
    GreaterEqual,
    Exclamation,
    ExclamationEqual,
    Semicolon,
    Newline,
    Eof,
}
